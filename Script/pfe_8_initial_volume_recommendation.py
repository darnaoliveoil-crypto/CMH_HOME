"""This step calculates the recommended values for various metrics based on the behavior of the `safe_start_ips`. It uses the median for 'initial volume', 'growth rate', 'time gap', and 'maximum safe volume' to get a central, robust estimate. For 'drops per day', it finds the mode (most frequent value) from the `df_ml` data during the safe period of these IPs. The results are then presented in a clear DataFrame called `recommendations_df`.

## STEP 4 — COMPUTE RECOMMENDATIONS
"""

# Compute recommendations from safe-start IPs
recommended_initial_volume = safe_start_ips['first_sent_per_ip'].median()
# For drops per day, we need to go back to the df_ml data for the safe IPs
# and then find the mode of drops_per_day during their safe period.
# However, the current structure of `safe_start_ips` does not directly contain `drops_per_day` aggregated.
# We need to consider how `drops_per_day` is defined and if it's per day or per event.
# From the initial problem description, it's 'drops per day', which is already aggregated daily in `df_ml`.
# Let's calculate the mode of 'drops_per_day' for the safe period of these safe_start_ips.

# Filter df_ml for the safe_start_ips
df_ml_safe_ips = df_ml[df_ml['ip'].isin(safe_start_ips['ip'])]

# Merge with initial_behavior_summary to get first_error_day for filtering safe period events
df_ml_safe_ips = pd.merge(df_ml_safe_ips, initial_behavior_summary[['ip', 'first_error_day']], on='ip', how='left')

# Filter events to only include the safe period for each IP
safe_period_events = df_ml_safe_ips[
    df_ml_safe_ips.apply(lambda row: pd.isna(row['first_error_day']) or row['datetime_gride'] < row['first_error_day'], axis=1)
]

# Calculate the mode of drops_per_day across all safe period events of safe-start IPs
# We take the first mode in case of multiple modes
recommended_drops_per_day = safe_period_events['drops_per_day'].mode()[0] if not safe_period_events['drops_per_day'].mode().empty else 0

recommended_growth_rate = safe_start_ips['average_safe_growth_rate'].median()
recommended_time_gap = safe_start_ips['average_time_gap_min'].median()
recommended_max_safe_volume = safe_start_ips['maximum_safe_volume'].median()

# Store recommendations in a DataFrame for clear display
recommendations_df = pd.DataFrame({
    'Metric': [
        'Recommended Initial Volume',
        'Recommended Drops Per Day',
        'Recommended Growth Rate',
        'Recommended Time Gap (min)',
        'Recommended Maximum Safe Volume'
    ],
    'Value': [
        recommended_initial_volume,
        recommended_drops_per_day,
        recommended_growth_rate,
        recommended_time_gap,
        recommended_max_safe_volume
    ]
})

print("Recommendations computed successfully.")
display(recommendations_df)

