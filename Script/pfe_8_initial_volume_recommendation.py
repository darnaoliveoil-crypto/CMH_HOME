"""This crucial step identifies IPs that meet the criteria for being considered 'safe-start' IPs. It filters the `initial_behavior_summary` based on four conditions:
- `first_launch_success == 1`: The IP had a successful initial launch.
- `first_error == 'No error'`: There were no initial errors recorded.
- `first_sent_per_ip < 10000`: The initial sending volume was below a certain threshold, indicating a cautious start.
- `safe_duration_days > 0`: The IP operated for at least some period without generating any errors.

The code then prints the number of identified safe-start IPs and displays their details. A diagnostic section is included to analyze the conditions if no safe-start IPs are found, helping to understand which criteria might be too strict.

## STEP 3 — SELECT SAFE-START IPS
"""

# Define safe-start IPs based on the criteria
safe_start_ips = initial_behavior_summary[
    (initial_behavior_summary['first_launch_success'] == 1) &
    (initial_behavior_summary['first_error'] == 'No error') &
    (initial_behavior_summary['first_sent_per_ip'] < 10000) &
    (initial_behavior_summary['safe_duration_days'] > 1) # Changed from >= 1 to > 0
].copy()

