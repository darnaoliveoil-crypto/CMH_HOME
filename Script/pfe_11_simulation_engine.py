def plot_simulation_results(df_simulation, title_suffix=""):
    """
    Plots the key metrics from the email sending IP simulation.
    """
    fig, axes = plt.subplots(nrows=3, ncols=2, figsize=(18, 18))
    fig.suptitle(f'Email Sending IP Strategy Simulation Results {title_suffix}', fontsize=16)

    # Plot 1: Daily Volume Evolution
    sns.lineplot(ax=axes[0, 0], x='Day', y='Daily Volume', data=df_simulation, marker='o')
    axes[0, 0].set_title('Daily Volume Evolution')
    axes[0, 0].set_ylabel('Emails Sent')

    # Plot 2: Cumulative Volume
    sns.lineplot(ax=axes[0, 1], x='Day', y='Cumulative Volume', data=df_simulation, marker='o', color='green')
    axes[0, 1].set_title('Cumulative Volume Sent')
    axes[0, 1].set_ylabel('Total Emails Sent')

    # Plot 3: Limit Usage Ratio
    sns.lineplot(ax=axes[1, 0], x='Day', y='Limit Usage Ratio', data=df_simulation, marker='o', color='purple')
    axes[1, 0].set_title('Daily Limit Usage Ratio')
    axes[1, 0].set_ylabel('Ratio of Limit Used')
    axes[1, 0].axhline(y=1.0, color='r', linestyle='--', label='Limit')
    axes[1, 0].legend()

    # Plot 4: Risk Probability Evolution
    sns.lineplot(ax=axes[1, 1], x='Day', y='Estimated Risk Probability', data=df_simulation, marker='o', color='orange')
    axes[1, 1].set_title('Estimated Risk Probability Evolution')
    axes[1, 1].set_ylabel('Risk Probability')
    axes[1, 1].axhline(y=0.4, color='orange', linestyle='--', label='Risk Threshold')
    axes[1, 1].axhline(y=0.7, color='red', linestyle='--', label='Dangerous Threshold')
    axes[1, 1].legend()

    # Plot 5: Error Probability Evolution
    sns.lineplot(ax=axes[2, 0], x='Day', y='Estimated Error Probability', data=df_simulation, marker='o', color='red')
    axes[2, 0].set_title('Estimated Error Probability Evolution')
    axes[2, 0].set_ylabel('Error Probability')
    axes[2, 0].axhline(y=0.05, color='orange', linestyle='--', label='Risk Threshold')
    axes[2, 0].axhline(y=0.15, color='red', linestyle='--', label='Dangerous Threshold')
    axes[2, 0].legend()

    # Plot 6: Risk Zone Evolution
    # Map risk zones to numerical values for plotting
    risk_zone_map = {'Safe': 0, 'Risk': 1, 'Dangerous': 2}
    df_simulation['Risk Zone Numerical'] = df_simulation['Predicted Risk Zone'].map(risk_zone_map)
    sns.scatterplot(ax=axes[2, 1], x='Day', y='Risk Zone Numerical', hue='Predicted Risk Zone', data=df_simulation, s=100, palette={'Safe': 'green', 'Risk': 'orange', 'Dangerous': 'red'})
    axes[2, 1].set_title('Predicted Risk Zone Evolution')
    axes[2, 1].set_yticks([0, 1, 2])
    axes[2, 1].set_yticklabels(['Safe', 'Risk', 'Dangerous'])
    axes[2, 1].set_ylabel('Risk Zone')
    axes[2, 1].get_legend().set_title('Zone')

    plt.tight_layout(rect=[0, 0.03, 1, 0.95])
    plt.show()

def plot_comparison(df_safe, df_dangerous, metric, title):
    """
    Compares a specific metric between two simulation scenarios.
    """
    plt.figure(figsize=(12, 7))
    sns.lineplot(x='Day', y=metric, data=df_safe, label='Safe Strategy', marker='o')
    sns.lineplot(x='Day', y=metric, data=df_dangerous, label='Dangerous Strategy', marker='o')
    plt.title(title)
    plt.ylabel(metric)
    plt.xlabel('Day')
    plt.legend()
    plt.grid(True)
    plt.show()

