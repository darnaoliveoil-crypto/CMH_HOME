"""---

### STEP 4 — Create Simulation Function

This function is the core of the simulation engine. It takes user-defined parameters and simulates the daily evolution of email sending metrics over a specified number of days.
"""

def simulate_ip_strategy(
    initial_volume,
    base_drops_per_day,
    base_growth_rate, # This is the 'base' growth rate; actual might be predicted
    limit,
    time_gap_between_drops,
    number_of_days_to_simulate
):
    """
    Simulates the daily evolution of an email sending IP strategy.

    Args:
        initial_volume (int): Starting email volume on Day 1.
        base_drops_per_day (int): Base number of email drops per day.
        base_growth_rate (float): Base daily volume growth rate (e.g., 0.05 for 5%).
        limit (int): Daily sending limit for the IP.
        time_gap_between_drops (int): Time in minutes between drops.
        number_of_days_to_simulate (int): Total number of days to simulate.

    Returns:
        pd.DataFrame: A DataFrame containing the simulated daily metrics.
    """

    simulation_results = []
    current_volume = initial_volume
    cumulative_volume = 0

    print(f"Starting simulation for {number_of_days_to_simulate} days...")

    for day in range(1, number_of_days_to_simulate + 1):
        # --- STEP 5: Simulate daily evolution ---
        # Get growth rate (can be predicted by model or use base)
        daily_growth_rate = get_growth_rate(day, base_growth_rate)

        if day == 1:
            daily_volume = initial_volume
        else:
            daily_volume = current_volume * (1 + daily_growth_rate)
        daily_volume = int(round(daily_volume)) # Ensure whole numbers for volume

        # Update current_volume for next day's calculation
        current_volume = daily_volume
        cumulative_volume += daily_volume

        # --- STEP 6: Compute metrics per day ---
        # Get actual drops_per_day (can be predicted by model or use base)
        drops_per_day = get_drops_per_day(daily_volume, limit, day, base_drops_per_day)

        volume_per_drop = daily_volume / drops_per_day if drops_per_day > 0 else 0

        # Limit usage ratio (capped at 1.0)
        limit_usage_ratio = min(daily_volume / limit, 1.0) if limit > 0 else 0.0

        # Estimated sent ratio (simple placeholder, could be 1 - error_probability)
        estimated_sent_ratio = 1.0 # Will be refined after error prediction

        # --- STEP 7: Predict risk and errors ---
        estimated_risk_probability = predict_risk_probability(
            volume=daily_volume,
            limit=limit,
            day_number=day,
            current_growth_rate=daily_growth_rate
        )
        estimated_error_probability = predict_error_probability(
            volume=daily_volume,
            limit=limit,
            day_number=day,
            risk_prob=estimated_risk_probability
        )

        # Refine estimated_sent_ratio based on error probability
        estimated_sent_ratio = max(0, 1.0 - estimated_error_probability)

        # --- STEP 8: Classify Safe/Risk/Dangerous ---
        predicted_risk_zone = classify_risk_zone(
            risk_probability=estimated_risk_probability,
            error_probability=estimated_error_probability
        )

        simulation_results.append({
            'Day': day,
            'Daily Volume': daily_volume,
            'Volume/Drop': round(volume_per_drop, 2),
            'Cumulative Volume': cumulative_volume,
            'Limit Usage Ratio': round(limit_usage_ratio, 3),
            'Growth Rate': round(daily_growth_rate, 4),
            'Estimated Sent Ratio': round(estimated_sent_ratio, 3),
            'Estimated Risk Probability': round(estimated_risk_probability, 3),
            'Estimated Error Probability': round(estimated_error_probability, 3),
            'Predicted Risk Zone': predicted_risk_zone
        })

    # --- STEP 9: Create final simulation dataframe ---
    df_simulation = pd.DataFrame(simulation_results)
    return df_simulation

