# Fill missing 'daily_volume' and 'risk_label_encoded' with 0 (or a suitable default)
df_full_dates['daily_volume'] = df_full_dates['daily_volume'].fillna(0)
# Fill missing 'sent_ratio' with the mean of the respective IP, or a global mean, or 0 if appropriate
# For simplicity, filling with 0, but a more sophisticated approach might be needed
