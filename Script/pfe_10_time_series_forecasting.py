"""### STEP 12 — Create reusable forecasting function

To make our forecasting process more efficient and reusable, we will create a function that can take any time series DataFrame (formatted with 'ds' and 'y' columns) and forecast future values using Prophet. This function will also handle model fitting, prediction, and return a clean forecast DataFrame.
"""

def create_prophet_forecast(df_ts, periods=7, seasonality=False, seasonality_mode='additive', cap_floor=False, model_name="Prophet Model"):
    """
    Trains a Prophet model and generates a forecast.

    Args:
        df_ts (pd.DataFrame): DataFrame with 'ds' (datestamp) and 'y' (target) columns.
        periods (int): Number of periods to forecast into the future.
        seasonality (bool): Whether to include daily seasonality.
        seasonality_mode (str): 'additive' or 'multiplicative' for seasonality.
        cap_floor (bool): Whether to include cap and floor values for logistic growth.
        model_name (str): Name for the model for printing/identification.

    Returns:
        pd.DataFrame: DataFrame containing the forecast, with 'ds', 'yhat', 'yhat_lower', 'yhat_upper' columns.
        Prophet model: The trained Prophet model.
    """
    print(f"\nTraining {model_name}...")

    # Initialize Prophet model
    model = Prophet(
        daily_seasonality=seasonality,
        weekly_seasonality=seasonality,
        yearly_seasonality=seasonality,
        seasonality_mode=seasonality_mode # 'additive' or 'multiplicative'
    )

    if cap_floor:
        # Ensure 'cap' and 'floor' columns exist if logistic growth is enabled
        # For simplicity, we'll assume y is always positive for cap/floor example.
        # In real scenarios, you'd define these based on domain knowledge.
        df_ts['cap'] = df_ts['y'].max() * 1.2 # 20% above max observed
        df_ts['floor'] = df_ts['y'].min() * 0.8 # 20% below min observed
        # Ensure cap is always greater than y
        df_ts['cap'] = df_ts['cap'].apply(lambda x: x if x > df_ts['y'].max() else df_ts['y'].max() + 1)
        model = Prophet(growth='logistic', daily_seasonality=seasonality, weekly_seasonality=seasonality, yearly_seasonality=seasonality, seasonality_mode=seasonality_mode)

    # Fit the model
    model.fit(df_ts)

    # Create future dataframe
    future = model.make_future_dataframe(periods=periods)
    if cap_floor:
        # For logistic growth, future dataframe also needs cap and floor
        future['cap'] = df_ts['cap'].iloc[-1] # Carry forward last known cap
        future['floor'] = df_ts['floor'].iloc[-1] # Carry forward last known floor

    # Make prediction
    forecast = model.predict(future)

    print(f"{model_name} trained and forecasted successfully!")
    return forecast[['ds', 'yhat', 'yhat_lower', 'yhat_upper']], model

print("Reusable forecasting function 'create_prophet_forecast' defined.")

