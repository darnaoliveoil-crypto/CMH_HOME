"""### STEP 14 — Train and Forecast with ARIMA Model (for comparison)

Now, let's train an ARIMA model using the `statsmodels` library for `daily_volume` to compare its performance with Prophet. Due to the very limited number of data points (only 2), choosing appropriate `p`, `d`, `q` orders for ARIMA is challenging and might lead to unstable models. We'll start with a simple order (e.g., 1, 0, 0) for demonstration purposes, acknowledging its limitations with so little data.
"""

# For ARIMA, the index should be datetime
# Make a copy to avoid modifying the original df_volume which is used by Prophet
df_volume_arima = df_volume.set_index('ds').copy()

# Due to only 2 data points, a complex ARIMA model will not fit.
# We'll try a simple ARIMA(1,0,0) model for demonstration, but results will be unreliable.
# 'd' (differencing) is typically 1 for non-stationary data, but with 2 points, it's hard to assess stationarity.
# 'p' (AR) and 'q' (MA) are also difficult to determine with so little data.

try:
    # Initialize and fit the ARIMA model
    # Setting enforce_stationarity=False and enforce_invertibility=False might be necessary for very short series
    model_arima_volume = ARIMA(df_volume_arima['y'], order=(1,0,0))
    model_arima_volume_fit = model_arima_volume.fit()

    print("ARIMA model for daily_volume trained successfully!")
    print(model_arima_volume_fit.summary())

    # Predict the next 7 days
    # Start prediction from the end of the training data
    start_idx = len(df_volume_arima)
    end_idx = start_idx + 6 # Forecast 7 days into the future
    forecast_arima_volume = model_arima_volume_fit.predict(start=start_idx, end=end_idx, typ='levels')

    # Create a DataFrame for ARIMA forecast results
    future_dates = pd.date_range(start=df_volume_arima.index.max() + pd.Timedelta(days=1), periods=7, freq='D')
    df_arima_forecast = pd.DataFrame({'ds': future_dates, 'yhat_arima': forecast_arima_volume})

    print("\nForecasted Daily Volume with ARIMA for the next 7 days:")
    display(df_arima_forecast.head(7))

except Exception as e:
    print(f"Could not train ARIMA model due to insufficient data or model complexity: {e}")
    model_arima_volume_fit = None
    df_arima_forecast = pd.DataFrame(columns=['ds', 'yhat_arima']) # Empty DataFrame if error

