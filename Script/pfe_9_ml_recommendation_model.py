"""## STEP 9 — Evaluate models

After training, it's crucial to evaluate the performance of our models on unseen data. We will use the test sets (`X_test`, `y_test`) created in Step 7. For each model and target, we'll calculate:
- **Mean Absolute Error (MAE):** The average of the absolute differences between predictions and actual values. It's a measure of prediction accuracy.
- **Mean Squared Error (MSE) / Root Mean Squared Error (RMSE):** Similar to MAE but penalizes larger errors more. RMSE is the square root of MSE and is in the same units as the target variable.
- **R-squared (R2 Score):** Represents the proportion of the variance in the dependent variable that is predictable from the independent variables. A higher R2 indicates a better fit.
"""

