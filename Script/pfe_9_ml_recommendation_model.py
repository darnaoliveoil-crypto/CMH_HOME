"""## STEP 5 — Aggregate `df_ml` per IP

To create a single row per IP for training, we need to aggregate the detailed `df_ml` data. This involves calculating summary statistics for key metrics like `drops_per_day`, `growth_rate`, `time_gap_min`, and `limit_usage_ratio` for each IP. These aggregated features will provide a concise representation of an IP's behavior over time.
"""

# Aggregate `df_ml` to get average `drops_per_day` for each IP
