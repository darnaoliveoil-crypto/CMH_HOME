"""## STEP 11 — Create function `recommend_new_ip_strategy()`

This function will serve as the core of our recommendation system. It will take in initial characteristics of a new IP (or a set of features that can be estimated for a new IP) and use the trained machine learning models to predict the recommended sending strategy (initial volume, growth rate, etc.).
"""

def recommend_new_ip_strategy(new_ip_features: pd.DataFrame, trained_models: dict, target_features: list) -> pd.DataFrame:
    """
    Recommends an initial sending strategy for a new IP based on trained ML models.

    Args:
        new_ip_features (pd.DataFrame): DataFrame containing features for the new IP.
                                        Must have columns matching the training features.
        trained_models (dict): Dictionary of trained models, structured as
                               {'target_name': {'model_name': model_object}}.
        target_features (list): List of feature names that were used for training.

    Returns:
        pd.DataFrame: A DataFrame with recommended values for each target variable.
    """
    recommendations = {}

    # Ensure the input features match the training features
    # We need to ensure that the new_ip_features DataFrame has all the columns
    # that were used for training, even if some values are placeholders for a new IP.
    # For this example, let's assume `new_ip_features` comes with all necessary columns.

    # Filter `new_ip_features` to only include the `target_features`
    input_features_for_prediction = new_ip_features[target_features]

    for target_name, models_for_target in trained_models.items():
        # For simplicity, we'll use the 'best' model as determined by R2,
        # or just pick RandomForestRegressor if R2 info isn't directly available here.
        # In a real-world scenario, you might have a mechanism to pick the best model.

        # Get the best model for the current target from evaluation_df
        best_model_info = evaluation_df[evaluation_df['Target'] == target_name].sort_values(by='R2', ascending=False).iloc[0]
        best_model_name = best_model_info['Model']

        model = models_for_target[best_model_name]

        # Make prediction
        prediction = model.predict(input_features_for_prediction)[0] # [0] because we expect a single prediction
        recommendations[f'recommended_{target_name}'] = prediction

    return pd.DataFrame([recommendations])

print("Function `recommend_new_ip_strategy` created successfully.")

